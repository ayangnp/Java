package Test01;

public class Example04 {
	public static void main (String [] args)
	{
		char a1 = 'd';
		char a2 = '@';
		int p1 = 97;
		int p2 = 45123;
		System.out.println("d在Unicode编码中的位置："+(int) a1);
		System.out.println("@在Unicode编码中的位置："+(int) a2);
		System.out.println("Unicode编码中97是：" +(char) p1);
		System.out.println("Unicode编码中45123是:" +(char) p2);
	}

}
